package nz.ac.wgtn.swen225.lc.app;

/**
 * Represents an input the app can receive.
 *
 * @author Jack Gallagher (300615528)
 */
public enum AppInput {
  /**
   * Move the player up.
   */
  MOVE_UP,

  /**
   * Move the player down.
   */
  MOVE_DOWN,

  /**
   * Move the player left.
   */
  MOVE_LEFT,

  /**
   * Move the player right.
   */
  MOVE_RIGHT,

  /**
   * Pause the game.
   */
  PAUSE,

  /**
   * Unpause the game.
   */
  UNPAUSE,
}
